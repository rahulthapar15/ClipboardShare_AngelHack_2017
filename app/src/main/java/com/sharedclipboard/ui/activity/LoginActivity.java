    /**
     * Represents an asynchronous login/registration task used to authenticate
     * the user.
     */
    public class UserLoginTask extends AsyncTask<Void, Void, Boolean> {

        private final String mEmail;
        private final String mPassword;

        UserLoginTask(String email, String password) {
            mEmail = email;
            mPassword = password;
        }

        @Override
        protected Boolean doInBackground(Void... params) {
            // TODO: attempt authentication against a network service.

            URL url;
            try {
                url = new URL(loginEndpoint);
            } catch (MalformedURLException e) {
                e.printStackTrace();
                throw new IllegalArgumentException("invalid url: " + loginEndpoint);
            }

            String body = "username=" + mEmail + "&password=" + mPassword;
            Log.d("Url", url.toString());

            byte[] bytes = body.getBytes();
            HttpURLConnection conn = null;
            try {
                conn = (HttpURLConnection) url.openConnection();
                conn.setDoOutput(true);
                conn.setUseCaches(false);
                conn.setFixedLengthStreamingMode(bytes.length);
                conn.setRequestMethod("POST");
                conn.setRequestProperty("Content-Type",
                        "application/x-www-form-urlencoded;charset=UTF-8");
                // post the request
                OutputStream out = conn.getOutputStream();
                out.write(bytes);
                out.close();

                // handle the response
                int status = conn.getResponseCode();
                Log.d("TAGG", "" + status);
                if(status == 202) {
                    Toast.makeText(getBaseContext(), "Invalid Password", Toast.LENGTH_SHORT).show();
                    return false;
                } else if (status != 200) {
                    return false;
                    //throw new IOException("Post failed with error code " + status);
                } else {
                    String passcode = conn.getHeaderField("Passcode");
                    Log.d("Passcode", passcode);
                    PreferenceUtils.putString(getBaseContext(), "passcode", passcode);
                    PreferenceUtils.putString(getBaseContext(), PreferenceUtils.PREF_EMAIL, mEmail);
                    Log.d("Passcode", PreferenceUtils.getString(getBaseContext(), "passcode","null"));
                }

                // Get Response
                InputStream is = conn.getInputStream();
                BufferedReader rd = new BufferedReader(new InputStreamReader(is));
                String line;
                StringBuffer response = new StringBuffer();
                while ((line = rd.readLine()) != null) {
                    response.append(line);
                    response.append('\n');
                }
                rd.close();
                return true;

            } catch(Exception e) {
                e.printStackTrace();
                return false;
            } finally {
                if (conn != null) {
                    conn.disconnect();
                }
            }


        }

        @Override
        protected void onPostExecute(final Boolean success) {
            mAuthTask = null;
            showProgress(false);

            if (success) {
                Intent intent = new Intent(getBaseContext(), MainActivity.class);
                startActivity(intent);
                finish();
            } else {
                mPasswordView.setError(getString(R.string.error_incorrect_password));
                mPasswordView.requestFocus();
            }
        }

        @Override
        protected void onCancelled() {
            mAuthTask = null;
            showProgress(false);
        }
    }
